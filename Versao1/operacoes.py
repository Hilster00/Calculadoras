#soma
def soma(v1,v2):
    return int(v1)+int(v2)

#subtracao
def subtracao(v1,v2):
    return int(v1)-int(v2)

#multiplicacao
def multiplicacao(v1,v2):
    return int(v1)*int(v2)

#divisao
def divisao(v1,v2):
    return int(v1)/int(v2)
    
#potencia    
def potencia(v1,v2):
    return int(v1)**int(v2)
    
#raiz    
def raiz(v1,v2):
    return abs(int(v1))**(1/int(v2))
    
